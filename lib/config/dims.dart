///
/// Class represent common used dimensions
/// based on [_baseline] dimension equal 8
///
class Dims {
  /// baseline
  static const double _baseline = 8;

  /// baseline / 2 = 4
  static const double tiny = _baseline / 2;

  /// baseline * 1 = 8
  static const double small = _baseline * 1;

  /// baseline * 2 = 16
  static const double medium = _baseline * 2;

  /// baseline * 3 = 24
  static const double large = _baseline * 3;

  /// baseline * 4 = 32
  static const double xlarge = _baseline * 4;

  /// baseline * 5 = 40
  static const double xxlarge = _baseline * 5;

  /// baseline * 6 = 48
  static const double xxxlarge = _baseline * 6;

  static const double radius = medium;
}
